package valueObject;

public class OLogin {

	private String id;
	private String password;
	// setters & getters 필요하다.
	public OLogin() {
		
	}
	public String getId() {
		return id;
	}
	public void setId(String id2) {
		this.id = id2;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password2) {
		this.password = password2;
	}

}
